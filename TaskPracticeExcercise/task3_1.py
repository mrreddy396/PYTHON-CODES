# Integer
age = 25

# Float
pi = 3.14159

# String
name = "Alice"

# Boolean
is_student = True

# List
fruits = ["apple", "banana", "orange"]

# Tuple
coordinates = (42.123, -71.456)

# Dictionary
person = {
    "name": "Bob",
    "age": 30,
    "is_employee": False
}

# Printing the variables and their types
print("Integer:", age, type(age))
print("Float:", pi, type(pi))
print("String:", name, type(name))
print("Boolean:", is_student, type(is_student))
print("List:", fruits, type(fruits))
print("Tuple:", coordinates, type(coordinates))
print("Dictionary:", person, type(person))